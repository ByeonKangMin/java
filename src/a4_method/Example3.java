package a4_method;

public class Example3 {
    public static void main(String[] args) {
        System.out.println(square(4));
        System.out.println(square(9));
        System.out.println(square(15));
    }
    public static int square(int ume) {
        //코드를 작성하여 매개변수의 제곱을 반환
        return ume*ume;
    }
}
